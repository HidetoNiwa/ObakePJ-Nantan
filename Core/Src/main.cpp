//-999<power<999 版set_power
void set_Power(int16_t power) {
	uint16_t PWM_A, PWM_B;
	if (power > 999) {
		power = 999;
	} else if (power < -999) {
		power = -999;
	}

	if (power > 0) {
		PWM_A = 999 - power;
		PWM_B = 999;
	} else {
		PWM_B = 999 + power;
		PWM_A = 999;
	}
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, PWM_A);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, PWM_B);
}

//-1<power<1 版set_power
void set_Power(float power) {
	uint16_t PWM_A, PWM_B;
	float val_A, val_B;
	if (power > 1) {
		power = 1;
	} else if (power < -1) {
		power = -1;
	}

	if (power > 0) {
		val_A = (1 - power) * 999.f;
		PWM_A = (uint16_t) val_A;
		PWM_B = 999;
	} else {
		val_B = (1 + power) * 999.f;
		PWM_B = (uint16_t) val_B;
		PWM_A = 999;
	}
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, PWM_A);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, PWM_B);
}

void delay_us(uint16_t us) {
	__HAL_TIM_SET_COUNTER(&htim7, 0);  // set the counter value a 0
	us = us;
	while (__HAL_TIM_GET_COUNTER(&htim7) < us)
		;  // wait for the counter to reach the us input in the parameter
}

//HCSR-04
float Distance = 0;
uint32_t IC_Val1 = 0;
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;  // is the first value captured ?

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {

	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3) // if the interrupt source is channel1
			{
		if (Is_First_Captured == 0) // if the first value is not captured
				{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3); // read the first value
			Is_First_Captured = 1;  // set the first captured as true
			// Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3,
					TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured == 1)   // if the first is already captured
				{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3); // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter

			if (IC_Val2 > IC_Val1) {
				Difference = IC_Val2 - IC_Val1;
			}

			else if (IC_Val1 > IC_Val2) {
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = (float) Difference * .034f / 2.f;
			Is_First_Captured = 0; // set it back to false

			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3,
					TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC3);
		}
	}
}

void HCSR04_get() {
	//float distance;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
	delay_us(2);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
	delay_us(10);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);

	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC3);

	HAL_Delay(40);
}

//set parameter
float set_reaction_distance(uint16_t val) {
	float distance = 0.f;
	distance = (float) val / 4096.f;
	distance *= 2000.f;
	return distance;
}

float set_kp_speed(uint16_t val) {
	float speed = 0.f;
	speed = (float) val / 4096.f;
	speed *= 3.f;
	return speed;
}

float set_max_length(uint16_t val) {
	float length = 0.f;
	length = (float) val / 4096.f;
	length *= 500.f;
	return length;
}

